#listas

# ['a', 'b', 'c', 'd', 'e', 'f] lista também é um abjeto da classe list que e sequencias mutaveis
# então podemos inclur excluir elementos inteiros dentro dela

lista = ['a', 'b', 'c', 'd', 'e', 'f']

lista.append('g')
print(lista)

lista.remove('g')
print(lista)

lista.reverse()
print(lista)

lista.sort(reverse=True) #sem parametro ela organiza em ordem alfabértica
print(lista)






























